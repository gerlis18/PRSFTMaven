    public void cargar() {
        try {
            DefaultTableModel model = new DefaultTableModel();
            TableColumn column;
            model.addColumn("ID Venta");
            model.addColumn("Cliente");
            model.addColumn("Fecha");
            model.addColumn("Estado");
            model.addColumn("Ver Factura");
            jTable1.setModel(model);
            column = jTable1.getColumnModel().getColumn(4);

            String consultar = "SELECT * FROM factura";
            String[] datos = new String[6];
            pst = cn.prepareStatement(consultar);
            rs = pst.executeQuery();
            while (rs.next()) {
                datos[0] = rs.getString("numFactura");
                datos[1] = rs.getString("Cliente_idCliente");
                datos[2] = rs.getString("fechaFactura");
                datos[3] = rs.getString("estadoFactura");
                column.setCellEditor(new MyEditor(jTable1));
                column.setCellRenderer(new MyRenderer(true));

                model.addRow(datos);
            }
            //  tbpro.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(InvVentas.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showInternalMessageDialog(this, ex.getMessage());
        }finally{
            try {
                pst.close();
                rs.close();
            } catch (SQLException ex) {
                Logger.getLogger(InvVentas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }

