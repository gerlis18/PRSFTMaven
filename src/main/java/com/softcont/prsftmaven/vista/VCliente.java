    private void tbclienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbclienteMouseClicked
        int fila = tbcliente.getSelectedRow();
        // condicion para 
        if(fila>=0){
           txtId.setText(tbcliente.getValueAt(fila, 0).toString());
           txtNombre.setText(tbcliente.getValueAt(fila, 1).toString());
           txtDireccion.setText(tbcliente.getValueAt(fila, 3).toString());
           txtTelefono.setText(tbcliente.getValueAt(fila, 2).toString());
           txtMail.setText(tbcliente.getValueAt(fila, 4).toString());
           btnBuscar.setEnabled(false);
           btnNuevo.setEnabled(false);
           btnRegistrar.setEnabled(false);
           btnModificar.setEnabled(true);
        }else{
          JOptionPane.showMessageDialog(null, "no a seleccionado ninguna fila");
        }
    }//GEN-LAST:event_tbclienteMouseClicked

