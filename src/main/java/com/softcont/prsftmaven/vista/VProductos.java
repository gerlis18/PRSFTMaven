    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            int resp = JOptionPane.showConfirmDialog(this, "¿Desea modificar producto?", "Modificar", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (JOptionPane.OK_OPTION == resp) {
                String nomp = txtNombre.getText();
                String codp = txtCodigo.getText();
                String preciop = txtPrecioVenta.getText();
                String precioCompra = txtPrecioCompra.getText();
                int cantp = getcantProducto();
                String cat = getCategoria();
                String Ruta = getRuta();
                //System.out.println(getRuta());
                modelo = new DAOProducto();
                switch (Ruta) {
                    case "no imagen":
                        String ruta = "no imagen";
                        modelo.modificarProducto(codp, nomp, cat, ruta);
                        modelo.numeros(txtCodigo);
                        modelo.cargar(jTable1);
                        limpiarCampos();
                        break;
                    default:
                        FileInputStream archivofoto;
                        archivofoto = new FileInputStream(getRuta());
                        modelo.modificarProducto(codp, nomp, cat, Ruta, archivofoto);
                        modelo.numeros(txtCodigo);
                        modelo.cargar(jTable1);
                        limpiarCampos();
                        break;
                }
            }

        } catch (HeadlessException | InstantiationException | IllegalAccessException | FileNotFoundException ex) {
            Logger.getLogger(VProductos.class.getName()).log(Level.OFF, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Error" + ex.getMessage());
        }

    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int resp = JOptionPane.showConfirmDialog(this, "¿Desea eliminar producto?", "Eliminar", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (JOptionPane.OK_OPTION == resp) {
            try {
                modelo = new DAOProducto();
                if (modelo.EliminarProd(txtCodigo.getText())==true) {
                    modelo.cargar(jTable1);
                    limpiarCampos();
                }else{
                    limpiarCampos();
                    btnEliminar.setEnabled(false);
                    btnRegistrar.setEnabled(true);
                    btnNuevo.setEnabled(false);
                    btnActualizar.setEnabled(false);
                    txtPrecioVenta.setText("");
                    jSpcantidad.setValue(0);
                    modelo.numeros(txtCodigo);
                }
                
                
            } catch (InstantiationException | IllegalAccessException ex) {
                Logger.getLogger(VProductos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

