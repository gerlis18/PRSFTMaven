    private void btnBuscarImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarImagenActionPerformed
        JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        fc.setDialogTitle("Abrir Archivo");
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Formatos de Archivos de Imagen(*.PNG;*.JPG;*.JPEG)", "jpg", "jpeg", "png");
        fc.setFileFilter(filtro);
        int seleccion = fc.showOpenDialog(this);

        if (seleccion == JFileChooser.APPROVE_OPTION) {
            try {
                File abre = fc.getSelectedFile();
                String ruta = abre.getPath();
                Image foto = getToolkit().getImage(ruta);
                foto = foto.getScaledInstance(jLimagen.getWidth(), jLimagen.getHeight(), Image.SCALE_DEFAULT);
                File carpeta = new File("ImgProductos");
                carpeta.mkdirs();
                if (carpeta.isDirectory()) {

                }
                jLimagen.setIcon(new ImageIcon(foto));
                vRuta.setText(ruta);
                btnBuscarImagen.setVisible(false);
                btnCambiarimagen.setVisible(true);
            } catch (Exception ex) {
                Logger.getLogger(VProductos.class.getName()).log(Level.OFF, null, ex);
            }
        }

    }//GEN-LAST:event_btnBuscarImagenActionPerformed

